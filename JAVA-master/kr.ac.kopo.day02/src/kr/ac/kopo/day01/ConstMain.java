package kr.ac.kopo.day01;

/*
 *  상수 : 5가지
 *  1. 정수형상수	: 10, 20. -678
 *  2. 실수형상수 : 1.2  3.45678
 *  3. 문자형상수: '3', 'A', 'm', '!', '가'
 *  4. 문자열상수 : "abc", "A"
 *  5. 논리형상수 : true, false
 */

public class ConstMain {

	public static void main(String[] args) {

		System.out.printf("%c%c\n", 'A', 'B');
		
		System.out.println('A' + 10);
		
		System.out.println('A' + 'B');
		
		final boolean BOOL = true;
		
		System.out.println(BOOL);
		
	}
}












